export default async function handler(req: NextApiRequest, res: NextApiResponse) {
  const { list } = req.query;

  const tokenUriRequestData = {
    address: "0xfd66C51e08Da68a8A48901FA0d981Df462340721",
    abi,
    functionName: "tokenURI",
  };

  const data = await readContracts({
    contracts: (list as string).split(",").map(tokenId => ({
      ...tokenUriRequestData,
      args: [tokenId],
    })),
  });

  const result = data.map(d => {
    const decodedDataString = Buffer.from((d as string).substring(29), "base64").toString();
    return JSON.parse(decodedDataString);
  });

  res.status(200).json(result);
}
